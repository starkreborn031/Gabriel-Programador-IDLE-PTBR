from math import radians, sin, cos, tan

ângulo = float(input('Digite o ângulo em graus para conversão: '))
seno = sin(radians(ângulo))
print('O SENO do ângulo {} é: {:.2f}'.format(ângulo, seno))
cosseno = cos(radians(ângulo))
print('O COSENO do ângulo {} é: {:.2f}'.format(ângulo, cosseno))
tangente = tan(radians(ângulo))
print('A TANGENTE do ângulo {} é: {:.2f}'.format(ângulo, tangente))


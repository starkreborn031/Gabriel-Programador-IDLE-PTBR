numero = int(input("Digite um número de 1 á 9: "))
dobro = numero * 2
triplo = numero * 3
raiz = numero ** (1/2)
print('O Dobro de {} é {}'.format(numero, dobro))
print('O Triplo de {} é {}'.format(numero, triplo))
print('A Raiz quadrada de {} é {:.2f}'.format(numero, raiz))

